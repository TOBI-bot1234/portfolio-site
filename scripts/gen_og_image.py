class PixelCanvas:
    def __init__(self, w, h, bg=BG):
        self.w = w
        self.h = h
        n = h * w
        self.r = [bg[0]] * n
        self.g = [bg[1]] * n
        self.b = [bg[2]] * n

    def px(self, x, y, color):
        if 0 <= x < self.w and 0 <= y < self.h:
            i = y * self.w + x
            self.r[i] = color[0]
            self.g[i] = color[1]
            self.b[i] = color[2]

    def hline(self, x1, y, x2, color):
        for x in range(min(x1, x2), max(x1, x2) + 1):
            self.px(x, y, color)

    def rect(self, x, y, w, h, color, fill=False):
        if fill:
            for dy in range(h):
                self.hline(x, y + dy, x + w - 1, color)
        else:
            self.hline(x, y, x + w - 1, color)
            self.hline(x, y + h - 1, x + w - 1, color)
            for dy in range(max(1, h - 1)):
                self.px(x, y + dy, color)
                self.px(x + w - 1, y + dy, color)

    def draw_text(self, text, x, y, scale=1, color=FG):
        glyphs = bitmap_char_glyphs()
        gx = x
        for ch in text:
            gly = glyphs.get(ch, glyphs.get(' ', [0] * 7))
            for row in range(7):
                byte = gly[row]
                for col in range(5):
                    if byte & (0x10 >> col):
                        self.rect(gx + col * scale, y + row * scale,
                                 scale, scale, color, fill=True)
            gx += 6 * scale

    def dashed_line(self, x1, y, x2, color, dash_len=8, gap_len=6):
        length = max(x2 - x1, 0)
        pos = x1
        while pos < x2:
            end = min(pos + dash_len, x2)
            self.hline(pos, y, end - 1, color)
            pos = end + gap_len

    def save(self, filename):
        def chunk(ctype, data):
            c = ctype + data
            crc = zlib.crc32(c) & 0xffffffff
            return struct.pack('>I', len(data)) + c + struct.pack('>I', crc)

        signature = b'\x89PNG\r\n\x1a\n'
        ihdr = chunk(b'IHDR', struct.pack('>IIBBBBB', self.w, self.h, 8, 2, 0, 0, 0))

        raw = b''
        for y in range(self.h):
            raw += b'\x00'
            base = y * self.w
            for x in range(self.w):
                raw += struct.pack('BBB', self.r[base + x],
                                  self.g[base + x], self.b[base + x])

        idat = chunk(b'IDAT', zlib.compress(raw, 9))
        iend = chunk(b'IEND', b'')

        with open(filename, 'wb') as f:
            f.write(signature + ihdr + idat + iend)
        print(f"Wrote {filename} ({self.w}x{self.h})")


def design_card():
    c = PixelCanvas(W, H)

    # Frame
    m = 56
    for dy in range(m, H - m):
        c.px(m, dy, FG)
        c.px(W - m - 1, dy, FG)
    for dx in range(m, W - m):
        c.px(dx, m, FG)
        c.px(dx, H - m - 1, FG)

    # Corner marks
    ck = 18
    for i in range(ck):
        c.px(m + 24 + i, m + 24, FG)
        c.px(m + 24, m + 24 + i, FG)
        c.px(W - m - 25 - i, H - m - 25, FG)
        c.px(W - m - 25, H - m - 25 - i, FG)

    # Main heading
    name_y = 200
    name = "OWEN KELLEY"
    scale = 14
    text_w = len(name) * 6 * scale
    text_x = (W - text_w) // 2
    c.draw_text(name, text_x, name_y, scale=scale, color=FG)

    # Subtitle
    sub = "ENGINEER & DESIGNER"
    sub_scale = 6
    sub_w = len(sub) * 6 * sub_scale
    sub_x = (W - sub_w) // 2
    sub_y = name_y + scale * 7 + 50
    c.draw_text(sub, sub_x, sub_y, scale=sub_scale, color=MUTED)

    # Dashed rule
    rule_y = sub_y + sub_scale * 7 + 40
    c.dashed_line((W - 200) // 2, rule_y, (W + 200) // 2, MUTED, dash_len=6, gap_len=4)

    # Tagline
    tag = "Building thoughtful software, one detail at a time."
    tag_scale = 3
    tag_w = len(tag) * 6 * tag_scale
    tag_x = (W - tag_w) // 2
    tag_y = rule_y + 35
    c.draw_text(tag, tag_x, tag_y, scale=tag_scale, color=MUTED)

    return c


if __name__ == '__main__':
    c = design_card()
    c.save('og-image.png')
